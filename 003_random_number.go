/*Програма відображає два числа в діапазоні 1-10. При передачі 10 Intn повертається число в проміжку 0-9, куди додається 1 і результат присвоюється до num.
Під час розгляду проміжку 1-10 обов'язково потрібно додати одиницю. В іншому випадку буде одержано число в діапазоні 0-9.*/

package main

import (
	"fmt"
	"math/rand"
)

func main() {
	var num = rand.Intn(10) + 1
	fmt.Println(num)

	num = rand.Intn(10) + 1
	fmt.Println(num)
}
