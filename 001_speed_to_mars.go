/*Як швидко повинна пересуватися ракета (км/год), щоб дістатися Марса
за 28 днів. Відстань від Землі до Марса дорівнює 56000000 км.*/

package main

import (
	"fmt"
)

func main() {

	const hours = 24 //кількість годин в добі
	var days = 28    // кількість днів польоту

	var distance = 56000000 //відстань до Марсу, км

	fmt.Println(distance/(days*hours), "км/год")

}
